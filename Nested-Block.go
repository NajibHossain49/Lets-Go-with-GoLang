/*

Nested Block (নেস্টেড ব্লক)

একটা ব্লকের ভেতরে আরেকটা ব্লক থাকতে পারে। ভেতরের ব্লক বাইরের ব্লকের ভ্যারিয়েবল অ্যাক্সেস করতে পারে, কিন্তু বাইরের ব্লক ভেতরের ভ্যারিয়েবল অ্যাক্সেস করতে পারে না।
যেমন, নিচের উদাহরণে inner ব্লক outer ব্লকের ভ্যারিয়েবল a কে অ্যাক্সেস করতে পারে, কিন্তু outer ব্লক inner ব্লকের ভ্যারিয়েবল b কে অ্যাক্সেস করতে পারে না।

*/ 


package main

import "fmt"

func main() {
    x := 100 // outer block

    {
        y := 200 // inner block
        fmt.Println("x:", x) // বাইরের ভ্যারিয়েবল অ্যাক্সেস করা যাবে
        fmt.Println("y:", y)
    }

    // fmt.Println(y) // ❌ Error: y শুধু ভেতরের ব্লকে সীমাবদ্ধ
}
// fmt.Println("y:", y) // ❌ Error: y কে বাইরের ব্লকে ব্যবহার করা যাবে না