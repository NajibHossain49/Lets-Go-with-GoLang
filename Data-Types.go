// Basic Data Types in Go
package main

import "fmt"

func main() {
	// Integer
	var i int = 42
	fmt.Println("Integer:", i)

	// Float
	var f float64 = 3.14
	fmt.Println("Float:", f)

	// String
	var s string = "Hello, Go!"
	fmt.Println("String:", s)

	// Boolean
	var b bool = true
	fmt.Println("Boolean:", b)
}
