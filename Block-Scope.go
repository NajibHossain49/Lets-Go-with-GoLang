// Block Scope

/*
ব্লক স্কোপ (Block Scope)

কোনো { } এর ভেতরে ডিক্লেয়ার করা ভ্যারিয়েবল শুধু সেই ব্লকের ভেতরেই ব্যবহার করা যায়। এটাকে বলে ব্লক স্কোপ।
যেমন, if, for, switch এর ভেতরে ডিক্লেয়ার করা ভ্যারিয়েবল শুধু সেই ব্লকের ভেতরেই ব্যবহার করা যাবে।

*/

package main 

import "fmt"

func main(){
	a := 10 

	if a > 5 {
		b := 20 
		fmt.Println(a, b) // এখানে অ্যাক্সেস করা যাবে
		
	}
	// fmt.Println(b) // ❌ Error: b কে if ব্লকের বাইরে ব্যবহার করা যাবে না
}