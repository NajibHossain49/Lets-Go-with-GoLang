// Local Scope is also known as Function Scope in Go.

/*

লোকাল স্কোপ (Local Scope)

Go-তে কোনো ভ্যারিয়েবল যদি ফাংশনের ভেতরে ডিক্লেয়ার করা হয়, তাহলে সেটি শুধুমাত্র সেই ফাংশনের ভেতরে ব্যবহার করা যাবে। এটাকে বলে লোকাল স্কোপ।
যেমন, main ফাংশনের ভেতরে ডিক্লেয়ার করা ভ্যারিয়েবল শুধু main ফাংশনের ভেতরেই ব্যবহার করা যাবে।

*/ 

package main

import "fmt"

func main() {
    x := 10   // লোকাল ভ্যারিয়েবল
    y := 20   // লোকাল ভ্যারিয়েবল

    fmt.Println(x, y) // এখানে অ্যাক্সেস করা যাবে
}

// fmt.Println(x) // ❌ Error: x কে main-এর বাইরে ব্যবহার করা যাবে না

