package main  
// Every Go program starts with a package declaration. "main" means this is the main package.

import "fmt"  
// "fmt" is a standard package in Go, used for printing and formatting text.

func main() {  
    // The main function is the entry point of the program. Execution starts here.
    
    fmt.Println("Hello, World!")  
    // fmt.Println prints text to the console. 
    // Here it will print: Hello, World!
}  
// This is how a very simple Go program works, printing one line of text to the console.
